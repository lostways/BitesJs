		for (i = 0; i < this.get(mapName).get('height'); i++) {
			for (j = 0; j < this.get(mapName).get('width'); j++) {
				switch (mapName) {
					case 'level2' :
						if (i === 25 && (j >= 20 && j<=60)) {
							this.get(mapName).get('map')[i][j] = 1;
						}
						break;
				}
			}
		}
		
